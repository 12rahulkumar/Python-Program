#  we can represent the int value in the decimal , binary , octal and hexadecimal form

# decimal number system = 0 to 9

a = 10
print(a)

# binary number system = 0 or 1 ---> prefix 0b/0B

a = 0b111
print(a)

# octal number system = 0 to 7  --->  prefix  0o/0O

a = 0o11
print(a)

# hexadecimal number system = 0 to 9, a to f  ---->   prefix  0x/0X

a = 0x11
print(a)