# fundamental data type vs immutability

# int
a = 10
b = 10

print(a is b)
print(id(a))
print(id(b))

#complex

c = 10+2j
d = 10+2j

print(c is d)
print(id(c))
print(id(d))

# boolean

e = True
f = False

print(e is f)
print(id(e))
print(id(f))

# str

g = 'rahul'
h = 'rahul'

print(g is h)
print(id(g))
print(id(h))